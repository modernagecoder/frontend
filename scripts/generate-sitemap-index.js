#!/usr/bin/env node
/**
 * generate-sitemap-index.js — split the crawl surface into per-cluster sitemaps.
 *
 * Why: one 738-URL sitemap makes GSC's "indexed vs submitted" almost useless. It tells you
 * "612 of 738 indexed" and nothing about WHICH 126 are missing. With per-cluster children,
 * "Discovered — not indexed" localises in one glance: a thin city cluster, or an orphaned
 * topic set, shows up as its own row instead of being averaged away.
 *
 * Design — deliberately additive, because sitemap.xml is the single most breakable SEO file
 * here and four scripts already read it (verify-sitemap, update-sitemap-lastmod,
 * generate-sitemap-html, indexnow-ping):
 *   - sitemap.xml stays exactly as it is: hand-maintained, and the SOURCE OF TRUTH.
 *   - This script only READS it, then derives the children and the index. Nothing to keep
 *     in sync by hand, and the children cannot drift from the source.
 *   - robots.txt advertises the index only. sitemap.xml stays served (harmless, and still
 *     what the tooling edits) but is no longer advertised, so the clusters are what get
 *     crawled and reported.
 *
 * OWNER ACTION to get the benefit: in GSC and Bing, submit /sitemap-index.xml and REMOVE the
 * old sitemap.xml submission. Leaving both submitted double-counts every URL and puts the
 * per-cluster stats back where they started. Removing a sitemap from GSC does not deindex
 * anything — the URLs are all still in the children.
 *
 * lastmod is copied verbatim from the source. It is never invented here; see
 * update-sitemap-lastmod.js for why that matters.
 */
const fs = require('fs');
const path = require('path');
const { cluster, CLUSTERS } = require('./sitemap-categories');

const ROOT = path.join(__dirname, '..');
const SITE = 'https://learn.modernagecoders.com';
const SOURCE = path.join(ROOT, 'sitemap.xml');
const RESOURCES = path.join(ROOT, 'content', 'resources', 'generated', 'sitemap-entries.xml');
const INDEX_OUT = path.join(ROOT, 'sitemap-index.xml');

function parseUrlBlocks(file) {
  if (!fs.existsSync(file)) return [];
  const xml = fs.readFileSync(file, 'utf8');
  const out = [];
  const re = /<url>[\s\S]*?<\/url>/g;
  let m;
  while ((m = re.exec(xml))) {
    const block = m[0];
    const loc = (block.match(/<loc>([\s\S]*?)<\/loc>/) || [])[1];
    if (!loc) continue;
    const lastmod = (block.match(/<lastmod>([\s\S]*?)<\/lastmod>/) || [])[1];
    out.push({ loc: loc.trim(), lastmod: lastmod && lastmod.trim(), block });
  }
  return out;
}

const urls = parseUrlBlocks(SOURCE);
if (!urls.length) {
  console.error('✗ No <url> entries found in sitemap.xml — refusing to write empty children.');
  process.exit(1);
}

// group
const groups = {};
CLUSTERS.filter((c) => !c.external).forEach((c) => (groups[c.id] = []));
const seen = new Set();
for (const u of urls) {
  if (seen.has(u.loc)) continue; // source is already deduped by verify:sitemap; belt and braces
  seen.add(u.loc);
  const id = cluster(u.loc);
  (groups[id] || groups.core).push(u);
}

// write children — each keeps the XSL stylesheet so the human view still works
let written = 0;
for (const c of CLUSTERS) {
  if (c.external) continue;
  const items = groups[c.id];
  const body = items
    .map((u) => {
      const lm = u.lastmod ? `\n    <lastmod>${u.lastmod}</lastmod>` : '';
      return `  <url>\n    <loc>${u.loc}</loc>${lm}\n  </url>`;
    })
    .join('\n');
  const xml =
    `<?xml version="1.0" encoding="UTF-8"?>\n` +
    `<?xml-stylesheet type="text/xsl" href="/sitemap.xsl"?>\n` +
    `<!-- ${c.note}. GENERATED by scripts/generate-sitemap-index.js from sitemap.xml.\n` +
    `     Do not hand-edit: add URLs to sitemap.xml, which is the source of truth. -->\n` +
    `<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">\n${body}\n</urlset>\n`;
  fs.writeFileSync(path.join(ROOT, c.file), xml);
  written++;
}

// resources sitemap is generated elsewhere; only reference it if it is really there
const resourceCount = parseUrlBlocks(RESOURCES).length;
const hasResources = fs.existsSync(path.join(ROOT, 'sitemap-resources.xml')) || resourceCount > 0;

const entries = CLUSTERS.filter((c) => !c.external || hasResources)
  .map((c) => `  <sitemap>\n    <loc>${SITE}/${c.file}</loc>\n  </sitemap>`)
  .join('\n');

fs.writeFileSync(
  INDEX_OUT,
  `<?xml version="1.0" encoding="UTF-8"?>\n` +
    `<!-- GENERATED by scripts/generate-sitemap-index.js. Submit THIS in GSC and Bing, and\n` +
    `     remove the old sitemap.xml submission so URLs are not counted twice. -->\n` +
    `<sitemapindex xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">\n${entries}\n</sitemapindex>\n`
);

// report
const total = [...seen].length;
let covered = 0;
console.log(`sitemap index written: ${written} child sitemap(s) + ${INDEX_OUT.split(/[\\/]/).pop()}`);
for (const c of CLUSTERS) {
  if (c.external) {
    console.log(`  ${c.file.padEnd(28)} ${String(resourceCount).padStart(4)}  (generated separately)`);
    continue;
  }
  covered += groups[c.id].length;
  console.log(`  ${c.file.padEnd(28)} ${String(groups[c.id].length).padStart(4)}`);
}
console.log(`  ${''.padEnd(28)} ----`);
console.log(`  ${'total from sitemap.xml'.padEnd(28)} ${String(covered).padStart(4)}`);

if (covered !== total) {
  console.error(`✗ URL loss: source had ${total}, children hold ${covered}.`);
  process.exit(1);
}
console.log(`✓ every one of the ${total} source URLs lands in exactly one child sitemap.`);
